# Commands
# ---------------------------------------------------------------------------


def cmd_promote(root: str, _args) -> int:
    raw = [m for m in scan_markers(root) if m.raw]
    if not raw:
        print("No raw TODOs to promote.")
        return 0
    nid = next_id(root)
    by_file: dict[str, list[Marker]] = {}
    for m in raw:
        by_file.setdefault(m.path, []).append(m)

    for rel in sorted(by_file):
        path = os.path.join(root, rel)
        lines = read_text(path).split("\n")
        for m in sorted(by_file[rel], key=lambda x: x.line):
            line = lines[m.line - 1]
            # Insert `#<id>` right after the leading `TODO` keyword.
            cut = m.col + len("TODO")
            lines[m.line - 1] = line[:cut] + f"#{nid}" + line[cut:]
            create_ticket(root, nid, m.msg, m.priority, m.area)
            print(f"  #{nid}  {rel}:{m.line}  {m.msg}")
            nid += 1
        write_text(path, "\n".join(lines))
    print(f"Promoted {len(raw)} TODO(s). Review the edits, then commit.")
    return 0


def cmd_new(root: str, args) -> int:
    if args.priority not in PRIORITIES:
        fail(f"priority must be one of {', '.join(PRIORITIES)}")
    tid = next_id(root)
    create_ticket(root, tid, args.message, args.priority, args.area or "")
    print(f"Created ticket #{tid} (no code anchor). Edit {TICKETS_DIR}/{tid}/ticket.md")
    return 0


def cmd_set(root: str, args) -> int:
    status = args.status.upper()
    if status not in STATUSES:
        fail(f"status must be one of {', '.join(STATUSES)}")
    update_status(root, args.id, status, args.comment)
    print(f"#{args.id} -> {status}")
    return 0


def cmd_note(root: str, args) -> int:
    tdir, _ = ticket_dir(root, args.id)
    if tdir is None:
        fail(f"ticket #{args.id} not found")
    md = os.path.join(tdir, "ticket.md")
    write_text(md, append_to_section(read_text(md), "## Log", f"- {today()} note: {args.comment}"))
    print(f"#{args.id} note added")
    return 0


def cmd_attach(root: str, args) -> int:
    tdir, _ = ticket_dir(root, args.id)
    if tdir is None:
        fail(f"ticket #{args.id} not found")
    src = args.file
    if not os.path.isfile(src):
        fail(f"no such file: {src}")
    size = os.path.getsize(src)
    if not args.binary:
        with open(src, "rb") as fh:
            looks_binary = b"\0" in fh.read(8192)
        if looks_binary:
            fail(f"{src} looks binary; pass --binary to attach it anyway")
        if size > ATTACH_SIZE_WARN:
            fail(f"{src} is {size // 1024} KiB (> {ATTACH_SIZE_WARN // 1024} KiB); pass --binary to confirm")
    name = os.path.basename(src)
    shutil.copy2(src, os.path.join(tdir, name))
    md = os.path.join(tdir, "ticket.md")
    write_text(md, append_to_section(read_text(md), "## Attachments", f"- {name}"))
    print(f"#{args.id} attached {name}")
    return 0


def cmd_show(root: str, args) -> int:
    tdir, closed = ticket_dir(root, args.id)
    if tdir is None:
        fail(f"ticket #{args.id} not found")
    print(read_text(os.path.join(tdir, "ticket.md")).rstrip())
    locs = [f"{m.path}:{m.line}" for m in scan_markers(root) if m.id == args.id]
    print("\nLive code references:" + (" (none)" if not locs else ""))
    for loc in locs:
        print(f"  {loc}")
    if closed:
        print("\n(closed)")
    return 0


def cmd_list(root: str, args) -> int:
    tickets = load_tickets(root)
    locs: dict[int, int] = {}
    for m in scan_markers(root):
        if m.id is not None:
            locs[m.id] = locs.get(m.id, 0) + 1

    rows = list(tickets.values())
    if args.status:
        rows = [t for t in rows if t.status == args.status.upper()]
    if args.area:
        rows = [t for t in rows if t.area == args.area]
    if args.priority:
        rows = [t for t in rows if t.priority == args.priority]
    if not rows:
        print("No tickets.")
        return 0

    rows.sort(key=lambda t: (STATUSES.index(t.status), PRIORITY_RANK.get(t.priority, 9), t.id))
    cur_status = None
    for t in rows:
        if t.status != cur_status:
            cur_status = t.status
            print(f"\n{cur_status}")
        area = f" [{t.area}]" if t.area else ""
        refs = locs.get(t.id, 0)
        print(f"  #{t.id:<3} {t.priority:<6}{area} {t.title}  ({refs} ref)")
    return 0


def _regen_refs(root: str) -> list[str]:
    """Regenerate every ticket's refs:auto block. Returns changed ticket.md paths."""
    locs: dict[int, list[str]] = {}
    for m in scan_markers(root):
        if m.id is not None:
            locs.setdefault(m.id, []).append(f"- {m.path}:{m.line}")
    changed = []
    for tid, t in load_tickets(root).items():
        md = os.path.join(t.dir, "ticket.md")
        text = read_text(md)
        updated = replace_refs(text, sorted(locs.get(tid, [])))
        if updated != text:
            write_text(md, updated)
            changed.append(md)
    return changed


def cmd_sync(root: str, args) -> int:
    changed = _regen_refs(root)
    for md in changed:
        print(f"synced {os.path.relpath(md, root)}")
        if args.stage:
            git_add(root, md)
    if not changed:
        print("Code references already up to date.")
    return 0


def cmd_close(root: str, args) -> int:
    tdir, closed = ticket_dir(root, args.id)
    if tdir is None:
        fail(f"ticket #{args.id} not found")
    if closed:
        fail(f"ticket #{args.id} is already closed")

    markers = [m for m in scan_markers(root) if m.id == args.id]
    by_file: dict[str, list[Marker]] = {}
    for m in markers:
        by_file.setdefault(m.path, []).append(m)

    print(f"Closing #{args.id} — removing {len(markers)} marker(s):")
    for m in markers:
        print(f"  {m.path}:{m.line}  {m.msg}")
    if args.dry_run:
        print("(dry run — nothing changed)")
        return 0

    for rel, ms in by_file.items():
        path = os.path.join(root, rel)
        token = lang_token(rel)
        lines = read_text(path).split("\n")
        drop: set[int] = set()
        for m in ms:
            idx = m.line - 1
            new = _strip_marker(lines[idx], token, m.col)
            if new is None:
                drop.add(idx)
            else:
                lines[idx] = new
        kept = [l for i, l in enumerate(lines) if i not in drop]
        write_text(path, "\n".join(kept))

    update_status(root, args.id, "CLOSED", args.comment)
    print(f"#{args.id} closed and archived to {TICKETS_DIR}/{CLOSED_SUBDIR}/{args.id}/")
    return 0


def _strip_marker(line: str, token: str, todo_col: int) -> Optional[str]:
    """Return the line with the trailing comment removed, or None to drop the line."""
    open_idx = line.find(token)
    if open_idx == -1 or open_idx > todo_col:
        open_idx = todo_col
    before = line[:open_idx]
    return None if before.strip() == "" else before.rstrip()


def cmd_check(root: str, _args) -> int:
    markers = scan_markers(root)
    tickets = load_tickets(root)
    errors: list[str] = []
    warnings: list[str] = []

    anchored_ids: set[int] = set()
    for m in markers:
        if m.id is None:
            continue
        anchored_ids.add(m.id)
        t = tickets.get(m.id)
        if t is None:
            errors.append(f"{m.path}:{m.line}: TODO#{m.id} has no ticket directory")
        elif t.closed:
            errors.append(f"{m.path}:{m.line}: TODO#{m.id} references a CLOSED ticket")

    for tid, t in tickets.items():
        if t.status in OPEN_STATUSES and tid not in anchored_ids:
            warnings.append(f"#{tid} is {t.status} but has no code marker")

    for rel, lineno, snippet in find_malformed(root):
        errors.append(f"{rel}:{lineno}: malformed TODO: {snippet}")

    for w in warnings:
        print(f"warning: {w}")
    for e in errors:
        print(f"error: {e}", file=sys.stderr)
    if errors:
        print(f"\n{len(errors)} error(s), {len(warnings)} warning(s)", file=sys.stderr)
        return 1
    print(f"OK ({len(tickets)} ticket(s), {len(warnings)} warning(s))")
    return 0


