def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(prog="ticket", description="Code-comment-backed ticket tracker.")
    sub = p.add_subparsers(dest="cmd", required=True)

    sub.add_parser("promote", help="turn raw TODOs into numbered tickets")

    sp = sub.add_parser("new", help="create a ticket without a code anchor")
    sp.add_argument("message")
    sp.add_argument("--priority", default="medium")
    sp.add_argument("--area", default="")

    sp = sub.add_parser("set", help="change status and append a log entry")
    sp.add_argument("id", type=int)
    sp.add_argument("status")
    sp.add_argument("comment", nargs="?")

    sp = sub.add_parser("note", help="append a log entry without changing status")
    sp.add_argument("id", type=int)
    sp.add_argument("comment")

    sp = sub.add_parser("attach", help="copy a file into the ticket directory")
    sp.add_argument("id", type=int)
    sp.add_argument("file")
    sp.add_argument("--binary", action="store_true", help="allow binary/large files")

    sp = sub.add_parser("show", help="render a ticket and its live code references")
    sp.add_argument("id", type=int)

    sp = sub.add_parser("list", help="list tickets")
    sp.add_argument("--status")
    sp.add_argument("--area")
    sp.add_argument("--priority")

    sp = sub.add_parser("sync", help="regenerate the refs:auto block in every ticket")
    sp.add_argument("--stage", action="store_true", help="git add changed ticket.md files")

    sp = sub.add_parser("close", help="remove the code marker(s) and archive the ticket")
    sp.add_argument("id", type=int)
    sp.add_argument("comment", nargs="?")
    sp.add_argument("--dry-run", action="store_true", help="preview removals only")

    sub.add_parser("check", help="integrity check (used by the pre-commit hook)")
    return p


COMMANDS = {
    "promote": cmd_promote,
    "new": cmd_new,
    "set": cmd_set,
    "note": cmd_note,
    "attach": cmd_attach,
    "show": cmd_show,
    "list": cmd_list,
    "sync": cmd_sync,
    "close": cmd_close,
    "check": cmd_check,
}


def main(argv: list[str]) -> int:
    args = build_parser().parse_args(argv)
    return COMMANDS[args.cmd](repo_root(), args)


