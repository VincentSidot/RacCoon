#!/usr/bin/env python3
"""Tiny, dependency-free ticket tracker backed by code comments.

Model
-----
A ticket is a directory under `.tickets/<id>/` whose `ticket.md` is the single
source of truth for mutable state (status, priority, area, notes, attachments).
The code comment is only an *anchor*:

    raw       TODO (<priority>[, <area>]): <message>
    promoted  TODO#<id> (<priority>[, <area>]): <message>

`file:line` locations are never stored by hand: they are resolved live by
grepping for `TODO#<id>`, and the committed `## Code references` block in each
ticket is regenerated (never hand-edited) by `sync`.

Lifecycle
---------
    write a raw TODO  ->  `promote`  ->  TODO#<id> + .tickets/<id>/
    `set <id> <STATUS> [comment]`     ->  update status + append to ## Log
    `close <id> [comment]`            ->  strip the code marker(s),
                                          status CLOSED, move to .tickets/closed/

This script is intentionally stdlib-only and edits `ticket.md` with targeted,
section-aware text surgery so hand-written prose is preserved across updates.
"""

from __future__ import annotations

import argparse
import os
import re
import shutil
import subprocess
import sys
from dataclasses import dataclass
from datetime import date
from typing import Optional

# ---------------------------------------------------------------------------
# Configuration (edit here to extend the tool)
# ---------------------------------------------------------------------------

TICKETS_DIR = ".tickets"
CLOSED_SUBDIR = "closed"

# Comment token per source language; also drives which files we scan.
COMMENT_TOKENS = {".zig": "//", ".s": ";"}

STATUSES = ("OPEN", "IN_PROGRESS", "BLOCKED", "CLOSED")
OPEN_STATUSES = ("OPEN", "IN_PROGRESS", "BLOCKED")
PRIORITIES = ("low", "medium", "high")
PRIORITY_RANK = {p: i for i, p in enumerate(("high", "medium", "low"))}

ATTACH_SIZE_WARN = 256 * 1024  # bytes; bigger needs --binary

REFS_BEGIN = "<!-- refs:auto — regenerated by scripts/ticket.py; do not edit below -->"
REFS_END = "<!-- /refs:auto -->"

# `TODO` not followed by `#` -> raw;   `TODO#<id>` -> promoted.
_FIELDS = r"\(\s*([A-Za-z]+)\s*(?:,\s*([^)]*?))?\s*\)\s*:\s*(.*)"
RAW_RE = re.compile(r"TODO(?!#)\s*" + _FIELDS)
PROMOTED_RE = re.compile(r"TODO#(\d+)\s*" + _FIELDS)
LOOSE_RE = re.compile(r"TODO(?:#\d+)?\s*\(")  # used only to flag malformed lines


# ---------------------------------------------------------------------------
# Small data types
# ---------------------------------------------------------------------------


@dataclass
class Marker:
    """A `TODO`/`TODO#id` occurrence found in a source file."""

    path: str  # repo-relative
    line: int  # 1-based
    col: int  # 0-based offset of the leading `TODO`
    raw: bool
    id: Optional[int]
    priority: str
    area: str
    msg: str


@dataclass
class Ticket:
    """Metadata read from a `.tickets/<id>/ticket.md` frontmatter."""

    id: int
    title: str
    status: str
    priority: str
    area: str
    dir: str
    closed: bool


# ---------------------------------------------------------------------------
# Generic helpers
# ---------------------------------------------------------------------------


def fail(msg: str) -> "NoReturn":  # type: ignore[name-defined]
    print(f"ticket: {msg}", file=sys.stderr)
    sys.exit(1)


def repo_root() -> str:
    try:
        out = subprocess.run(
            ["git", "rev-parse", "--show-toplevel"],
            capture_output=True, text=True, check=True,
        )
    except (subprocess.CalledProcessError, FileNotFoundError):
        fail("not inside a git repository")
    return out.stdout.strip()


def read_text(path: str) -> str:
    with open(path, "r", encoding="utf-8") as fh:
        return fh.read()


def write_text(path: str, text: str) -> None:
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(text)


def today() -> str:
    return date.today().isoformat()


def git_add(root: str, *paths: str) -> None:
    subprocess.run(["git", "-C", root, "add", "--", *paths], check=False)


def tracked_files(root: str) -> list[str]:
    """Tracked + untracked-but-not-ignored files (respects .gitignore)."""
    out = subprocess.run(
        ["git", "-C", root, "ls-files", "--cached", "--others", "--exclude-standard"],
        capture_output=True, text=True, check=True,
    )
    # De-duplicate while preserving order (a path can appear cached and other).
    seen, files = set(), []
    for rel in out.stdout.splitlines():
        if rel and rel not in seen:
            seen.add(rel)
            files.append(rel)
    return files


def lang_token(rel: str) -> Optional[str]:
    return COMMENT_TOKENS.get(os.path.splitext(rel)[1].lower())


# ---------------------------------------------------------------------------
# Scanning source for markers
# ---------------------------------------------------------------------------


def scan_markers(root: str) -> list[Marker]:
    markers: list[Marker] = []
    for rel in tracked_files(root):
        if lang_token(rel) is None:
            continue
        try:
            text = read_text(os.path.join(root, rel))
        except (OSError, UnicodeDecodeError):
            continue
        for lineno, line in enumerate(text.split("\n"), start=1):
            m = PROMOTED_RE.search(line)
            if m:
                markers.append(Marker(
                    rel, lineno, m.start(), False, int(m.group(1)),
                    m.group(2).lower(), (m.group(3) or "").strip(), m.group(4).strip(),
                ))
                continue
            m = RAW_RE.search(line)
            if m:
                markers.append(Marker(
                    rel, lineno, m.start(), True, None,
                    m.group(1).lower(), (m.group(2) or "").strip(), m.group(3).strip(),
                ))
    return markers


def find_malformed(root: str) -> list[tuple[str, int, str]]:
    """Lines that look like a TODO marker but parse as neither raw nor promoted."""
    bad = []
    for rel in tracked_files(root):
        if lang_token(rel) is None:
            continue
        try:
            text = read_text(os.path.join(root, rel))
        except (OSError, UnicodeDecodeError):
            continue
        for lineno, line in enumerate(text.split("\n"), start=1):
            if LOOSE_RE.search(line) and not (RAW_RE.search(line) or PROMOTED_RE.search(line)):
                bad.append((rel, lineno, line.strip()))
    return bad


# ---------------------------------------------------------------------------
# Ticket store
# ---------------------------------------------------------------------------


def tickets_base(root: str) -> str:
    return os.path.join(root, TICKETS_DIR)


def ticket_dir(root: str, tid: int) -> tuple[Optional[str], bool]:
    """Return (dir, is_closed) for a ticket, or (None, False) if absent."""
    open_dir = os.path.join(tickets_base(root), str(tid))
    closed_dir = os.path.join(tickets_base(root), CLOSED_SUBDIR, str(tid))
    if os.path.isdir(open_dir):
        return open_dir, False
    if os.path.isdir(closed_dir):
        return closed_dir, True
    return None, False


def existing_ids(root: str) -> set[int]:
    ids: set[int] = set()
    base = tickets_base(root)
    for parent in (base, os.path.join(base, CLOSED_SUBDIR)):
        if os.path.isdir(parent):
            for name in os.listdir(parent):
                if name.isdigit() and os.path.isdir(os.path.join(parent, name)):
                    ids.add(int(name))
    return ids


def next_id(root: str) -> int:
    ids = existing_ids(root)
    return max(ids) + 1 if ids else 1


def load_tickets(root: str) -> dict[int, Ticket]:
    out: dict[int, Ticket] = {}
    base = tickets_base(root)
    for closed, parent in ((False, base), (True, os.path.join(base, CLOSED_SUBDIR))):
        if not os.path.isdir(parent):
            continue
        for name in sorted(os.listdir(parent)):
            tdir = os.path.join(parent, name)
            md = os.path.join(tdir, "ticket.md")
            if not (name.isdigit() and os.path.isfile(md)):
                continue
            fm = parse_frontmatter(read_text(md))
            tid = int(name)
            out[tid] = Ticket(
                id=tid,
                title=fm.get("title", ""),
                status=fm.get("status", "OPEN"),
                priority=fm.get("priority", "medium"),
                area=fm.get("area", ""),
                dir=tdir,
                closed=closed,
            )
    return out


def ticket_template(tid: int, title: str, priority: str, area: str, created: str) -> str:
    return (
        f"---\n"
        f"id: {tid}\n"
        f"title: {title}\n"
        f"status: OPEN\n"
        f"priority: {priority}\n"
        f"area: {area}\n"
        f"created: {created}\n"
        f"---\n\n"
        f"## Description\n\n"
        f"{title}\n\n"
        f"## Log\n\n"
        f"- {created} created (OPEN)\n\n"
        f"## Attachments\n\n"
        f"## Code references\n\n"
        f"{REFS_BEGIN}\n"
        f"{REFS_END}\n"
    )


def create_ticket(root: str, tid: int, title: str, priority: str, area: str) -> str:
    tdir = os.path.join(tickets_base(root), str(tid))
    os.makedirs(tdir, exist_ok=True)
    write_text(os.path.join(tdir, "ticket.md"), ticket_template(tid, title, priority, area, today()))
    return tdir


# ---------------------------------------------------------------------------
# ticket.md text surgery (frontmatter / sections / refs block)
# ---------------------------------------------------------------------------


def _join(lines: list[str]) -> str:
    text = "\n".join(lines)
    return text if text.endswith("\n") else text + "\n"


def parse_frontmatter(text: str) -> dict[str, str]:
    lines = text.split("\n")
    if not lines or lines[0].strip() != "---":
        return {}
    fm: dict[str, str] = {}
    for line in lines[1:]:
        if line.strip() == "---":
            break
        if ":" in line:
            key, value = line.split(":", 1)
            fm[key.strip()] = value.strip()
    return fm


def set_frontmatter_field(text: str, key: str, value: str) -> str:
    lines = text.split("\n")
    if not lines or lines[0].strip() != "---":
        fail("ticket.md has no frontmatter")
    end = next((i for i in range(1, len(lines)) if lines[i].strip() == "---"), None)
    if end is None:
        fail("ticket.md frontmatter is not terminated")
    for i in range(1, end):
        if ":" in lines[i] and lines[i].split(":", 1)[0].strip() == key:
            lines[i] = f"{key}: {value}"
            break
    else:
        lines.insert(end, f"{key}: {value}")
    return _join(lines)


def append_to_section(text: str, header: str, entry: str) -> str:
    """Insert `entry` at the end of the `## Header` section (before next `## `)."""
    lines = text.split("\n")
    try:
        start = next(i for i, l in enumerate(lines) if l.strip() == header)
    except StopIteration:
        # Section missing: append it at the end.
        return _join(lines + ["", header, "", entry])
    end = next((i for i in range(start + 1, len(lines)) if lines[i].startswith("## ")), len(lines))
    while end - 1 > start and lines[end - 1].strip() == "":
        end -= 1
    lines.insert(end, entry)
    return _join(lines)


def replace_refs(text: str, ref_lines: list[str]) -> str:
    lines = text.split("\n")
    begin = next((i for i, l in enumerate(lines) if l.strip().startswith("<!-- refs:auto")), None)
    end = next((i for i, l in enumerate(lines) if l.strip() == REFS_END), None)
    if begin is None or end is None or end < begin:
        return _join(lines + ["", "## Code references", "", REFS_BEGIN, *ref_lines, REFS_END])
    return _join(lines[: begin + 1] + ref_lines + lines[end:])


# ---------------------------------------------------------------------------
# Status transitions (shared by `set` and `close`)
# ---------------------------------------------------------------------------


def reconcile_dir(root: str, tid: int, status: str) -> str:
    """Move a ticket between the open area and `closed/` to match its status."""
    cur, closed = ticket_dir(root, tid)
    if cur is None:
        fail(f"ticket #{tid} not found")
    want_closed = status == "CLOSED"
    if want_closed and not closed:
        dst = os.path.join(tickets_base(root), CLOSED_SUBDIR, str(tid))
        os.makedirs(os.path.dirname(dst), exist_ok=True)
        shutil.move(cur, dst)
        return dst
    if not want_closed and closed:
        dst = os.path.join(tickets_base(root), str(tid))
        shutil.move(cur, dst)
        return dst
    return cur


def update_status(root: str, tid: int, new_status: str, comment: Optional[str]) -> None:
    tdir, _ = ticket_dir(root, tid)
    if tdir is None:
        fail(f"ticket #{tid} not found")
    md = os.path.join(tdir, "ticket.md")
    text = read_text(md)
    old = parse_frontmatter(text).get("status", "OPEN")
    text = set_frontmatter_field(text, "status", new_status)
    transition = f"{old} -> {new_status}" if old != new_status else new_status
    log = f"- {today()} {transition}" + (f": {comment}" if comment else "")
    text = append_to_section(text, "## Log", log)
    write_text(md, text)
    reconcile_dir(root, tid, new_status)


